(function () {//->IIFE
  console.log("A cozy nest is ready");
})();
/*
  Распространенным шаблоном в JavaScript является выполнение функции сразу после ее объявления:
  
  Note that the function has no name and is not stored in a variable.
  The two parentheses () at the end of the function expression cause it to be immediately 
  executed or invoked. This pattern is known as an immediately invoked function expression or IIFE.
*/
